public static class FungusTextUtils
{
    public static string GetRandomColor() =>
        $"#{UnityEngine.Random.Range(0, 255):X}{UnityEngine.Random.Range(0, 255):X}{Random.Range(0, 255):X}";

    public static string Bold(this        string str)             => $"{{b}}{str}{{/b}}";
    public static string RandomColor(this string str)             => str.Color(GetRandomColor());
    public static string Color(this       string str, string clr) => $"{{color={clr}}}{str}{{/color}}";
    public static string Italic(this      string str)           => $"{{i}}{str}{{/i}}";
    public static string Size(this        string str, int size) => $"{{size={size}}}{str}{{/size}}";
    public static string Bold(this        char   str)             => $"{{b}}{str}{{/b}}";
    public static string Color(this       char   str, string clr) => $"{{color={clr}}}{str}{{/color}}";
    public static string Italic(this      char   str)           => $"{{i}}{str}{{/i}}";
    public static string Size(this        char   str, int size) => $"{{size={size}}}{str}{{/size}}";

    public static string TextHelper(this string text, int size, Func<string, string> callback)
    {
        var sb = new StringBuilder();
        if (size == 1)
        {
            foreach (var item in text)
            {
                sb.Append(callback.Invoke(item.ToString()));
            }
        }
        else
        {
            var length = text.Length;
            var re     = text.Length % size;
            var count  = (length - re) / size;

            var num = 0;
            for (var i = 0; i < count; i++)
            {
                sb.Append(callback.Invoke(text.Substring(num, size)));
                num += size;
            }

            if (re > 0 && num < length)
            {
                sb.Append(callback.Invoke(text.Substring(num, re)));
            }
        }

        return sb.ToString();
    }
